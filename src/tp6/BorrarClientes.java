    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
           long Dni= Integer.parseInt(txfDni.getText());   
           TreeMap<String, Cliente> lista= Menu.getDirectorio().borrarCliente(Dni);
           Set<String> clave=lista.keySet();
           for(String claves:clave){
               Cliente c=lista.get(claves);
               if(c.getDni()==Dni){
                txfTelefono.setText(claves);
                txfApellido.setText(c.getApellido());
                txfNombre.setText(c.getNombre());
                txfCiudad.setText(c.getCiudad());
                
                JOptionPane.showMessageDialog(this,"Cliente Borrado");

            }else {
                   JOptionPane.showMessageDialog(this,"Cliente no encontrado.");
               }
           }
           
    }//GEN-LAST:event_btnBorrarActionPerformed

